void	ft_sort_int_tab(int *tab, int size)
{
	int a;
	int i;
	int j;

	i = 0;
	while (i++ < size)
	{
		j = 0;
		while (++j < size)
		{
			if (tab[j - 1] > tab[j])
			{
				a = tab[j - 1];
				tab[j - 1] = tab[j];
				tab[j] = a;
			}
		}
	}
}
